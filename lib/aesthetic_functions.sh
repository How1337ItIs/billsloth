show_matrix_effect() {
    local lines="${1:-10}"
    local delay="${2:-0.1}"
    
    for i in $(seq 1 $lines); do
        echo -e "${MATRIX_GREEN}$(head /dev/urandom | tr -dc 'A-Za-z0-9!@#$%^&*()' | head -c 80)${RESET}"
        sleep $delay
    done
}

show_typing_effect() {
    local text="$1"
    local delay="${2:-0.05}"
    local color="${3:-$BRIGHT_GREEN}"
    
    echo -n -e "$color"
    for (( i=0; i<${#text}; i++ )); do
        echo -n "${text:$i:1}"
        sleep $delay
    done
    echo -e "${RESET}"
}

show_countdown() {
    local seconds="$1"
    local message="${2:-Starting in}"
    
    for (( i=seconds; i>0; i-- )); do
        printf "\r${BRIGHT_YELLOW}$message $i...${RESET}"
        sleep 1
    done
    printf "\r${BRIGHT_GREEN}$message NOW!${RESET}\n"
}

